package spellchecker;

public class DictionaryTriplet {
		private String first;
		private double second;
		private boolean third;

	public DictionaryTriplet(String first, double second, boolean third) {
		this.first = first;
		this.second = second;
		this.third = third;
	}

	public String getFirst() {
		return first;
	}

	public double getSecond() {
		return second;
	}
	
	public boolean getThird() {
		return third;

	}
}
